
package ejemploapilang;
import java.lang.*; // De esta forma a√±adimos la utilidad completa de de la API "lang" 
// Aqui podremos consultar la documentacion de la API lang : https://docs.oracle.com/javase/8/docs/api/java/lang/package-summary.html


public class EjemploApiLang {

    
    public static void main(String[] args) {
        double raiz = Math.sqrt(16); // Con el metodo Math.sqrt de la API "lang" podemos sacar la raiz cuadrada del numero que le inidquemos
        System.out.println(raiz);
        int numeroAbs = Math.abs(50); // Con el metodo Math.abs podemos sacar el numero absoluto del numero que le indiquemos. 
        System.out.println(numeroAbs);
        
    }
    
}
